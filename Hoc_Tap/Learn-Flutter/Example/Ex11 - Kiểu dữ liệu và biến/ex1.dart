
void main() {
  int a = 5;
  print(a.runtimeType); // Output: int
  print(a); // Output: 5
  print('----------------------');
  // Khai báo biến với kiểu dữ liệu num
  num b = 5;
  print(b.runtimeType); // Output: int
  print(b); // Output: 5
  print('----------------------');
  // Khai báo biến với kiểu dữ liệu double
  double c = 5.0;
  print(c.runtimeType); // Output: double 
  print(c); // Output: 5.0
  print('----------------------');
  // Khai báo biến với kiểu dữ liệu String
  String d = 'Hello, Dart!';
  print(d.runtimeType); // Output: String
  print(d); // Output: Hello, Dart!
  print('----------------------');
  // Khai báo biến với kiểu dữ liệu bool
  bool e = true;  
  print(e.runtimeType); // Output: bool
  print(e); // Output: true
  print('----------------------');

}
